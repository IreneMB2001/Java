/*VariablesConcatenadas.java
*Programa que muestra por pantalla una variable concatenada con otras 5 variables char.
*@Irene Martin
*/

public class VariablesConcatenadas {
	public static void main (String [] args) {
		char m= 'm';
		char o= 'o';
		char r= 'r';
		char a= 'a';
		char d= 'd';
		String cadena= "" + m + o + r + a + d + o;
		System.out.println (" Mi color favorito es el: " +cadena);
		
	}
}
