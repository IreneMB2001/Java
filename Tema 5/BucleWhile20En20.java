public class BucleWhile20En20 {
  public static void main (String [] args) {
    System.out.println (" ");
    int i=320;
    System.out.println (" Numeros de 320 a 160, de 20 en 20, con el bucle WHILE");
    while (i>=160) {
      i=i-20; {
        System.out.println (" " +i);
      } 
      i++;
    }
  }
}
