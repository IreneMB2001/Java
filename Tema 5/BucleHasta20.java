public class BucleHasta20 {
  public static void main (String [] args) {
    
    System.out.println (" ");
    
    System.out.println(" 20 primeros numeros:");
    for (int i=0; i<=20; i=i+2) {
      System.out.println (" " +i);
    }
  }
}
