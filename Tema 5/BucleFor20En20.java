/*BucleFor20En20.java
*Programa que muestra por pantalla los numeros de 320 a 160, de 20 en 20, con el bucle FOR.
*@Irene Martin
*/

public class BucleFor20En20 {
  public static void main (String [] args) {
    System.out.println (" ");
    System.out.println (" Numeros de 320 a 160, de 20 en 20, con el bucle FOR");
    for (int i=320; i>=160; i=i-20) {
 
      System.out.println (" " +i);
    }
  }
}
